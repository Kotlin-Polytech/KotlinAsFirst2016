 * Например, 54 и 323 состоят из разных цифр, а 111 и 0 из одинаковых.
 */
fun hasDifferentDigits(n: Int): Boolean {
    var number = n
    var k = number % 10
    if (n == 0) return false else
        while (number > 0) {
            if (k != number % 10) {
                return true
            }
            k = number % 10
            number /= 10
        }
    return false
}

