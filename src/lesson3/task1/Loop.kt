/**
 * Средняя
 *
 * Поменять порядок цифр заданного числа n на обратный: 13478 -> 87431.
 * Не использовать строки при решении задачи.
 */
fun revert(n: Int): Int {
    var k = digitNumber(n) - 1
    var revertNumber = 0
    var number = n
    while (k >= 0) {
        revertNumber = revertNumber + pow(10, k) * (number % 10)
        number = number / 10
        k--
    }
    return revertNumber
}

/**
