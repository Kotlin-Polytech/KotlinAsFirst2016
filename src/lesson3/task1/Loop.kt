 */
fun fibSequenceDigit(n: Int): Int {
    var fibonach = 0
    var sum = 0
    var count = 1
    var number = 0.0
    while (sum < n) {
        fibonach = fib(count)
        count++
        sum = sum + digitNumber(fibonach)
    }
    count = sum - n
    number = fibonach.toDouble() / pow(10.0, (count).toDouble())
    return number.toInt() % 10
}
