 */
fun factorizeToString(n: Int): String = factorize(n).joinToString(separator = "*")


/**
 * Средняя
 *
 * Перевести заданное натуральное число n в систему счисления с основанием base > 1.
 * Результат перевода вернуть в виде списка цифр в base-ичной системе от старшей к младшей,
 * например: n = 100, base = 4 -> (1, 2, 1, 0) или n = 250, base = 14 -> (1, 3, 12)
 */
fun convert(n: Int, base: Int): List<Int> {
    var number = n
    var res = listOf<Int>()
    if (n != 0) {
        while (number != 0) {
            res += number % base
            number /= base
        }
        return res.reversed()
    } else {
        res += 0
        return res
    }
}

/**
 * Сложная
 *
 * Перевести заданное натуральное число n в систему счисления с основанием 1 < base < 37.
 * Результат перевода вернуть в виде строки, цифры более 9 представлять латинскими
 * строчными буквами: 10 -> a, 11 -> b, 12 -> c и так далее.
 * Например: n = 100, base = 4 -> 1210, n = 250, base = 14 -> 13c
 */
fun numberToSymbol(n: Int): String =
        if (n >= 10) ('a' + (n - 10)).toString()
        else n.toString()

fun convertToString(n: Int, base: Int): String =
        convert(n, base).map { numberToSymbol(it) }.joinToString(separator = "")


