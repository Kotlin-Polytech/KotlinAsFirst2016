 * 10 -> a, 11 -> b, 12 -> c и так далее.
 * Например: str = "13c", base = 14 -> 250
 */
fun symbolToNumber(str: Char): Int =
        if (str <= '9') str - '0'
        else str - 'a' + 10

fun decimalFromString(str: String, base: Int): Int = decimal(str.map { symbolToNumber(it) }, base)

/**
 * Сложная
 *
 * Перевести натуральное число n в римскую систему.
 * Римские цифры: 1 = I, 4 = IV, 5 = V, 9 = IX, 10 = X, 40 = XL, 50 = L,
 * 90 = XC, 100 = C, 400 = CD, 500 = D, 900 = CM, 1000 = M.
 * Например: 23 = XXIII, 44 = XLIV, 100 = C
 */

fun roman(n: Int): String {
    val listAll = listOf("I" to 1, "IV" to 4, "V" to 5, "IX" to 9, "X" to 10, "XL" to 40, "L" to 50,
            "XC" to 90, "C" to 100, "CD" to 400, "D" to 500, "CM" to 900, "M" to 1000)
    var number = n
    if (number <= 0) return ""
    else {
        var res = ""
        while (number > 0) {
            for (i in listAll.size - 1 downTo 0) {
                if (number - listAll[i].second >= 0) {
                    res += listAll[i].first
                    number -= listAll[i].second
                    break
                }
            }
        }
        return res
    }
}

