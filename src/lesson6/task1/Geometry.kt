fun polarAngle(p0: Point, p1: Point, p2: Point): Double {
    val a = (p1.x - p0.x) * (p2.y - p0.y) - (p2.y - p1.y) * (p2.x - p0.x)
    if (a == 0.0) return  length(p0, p2) - length(p0, p1)
