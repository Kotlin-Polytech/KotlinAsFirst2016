 */
fun circleByThreePoints(a: Point, b: Point, c: Point): Circle {
    val BissAC = bisectorByPoints(a, c)
    val BissAB = bisectorByPoints(a, b)
    return Circle(BissAB.crossPoint(BissAC), a.distance(BissAB.crossPoint(BissAC)))
}


